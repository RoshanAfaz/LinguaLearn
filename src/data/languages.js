// API service to fetch languages from backend
export const fetchLanguages = async () => {
  try {
    const response = await fetch(`${import.meta.env.VITE_API_URL || 'http://localhost:5000/api'}/languages`);
    const data = await response.json();
    return data.success ? data.data : languages;
  } catch (error) {
    console.error('Error fetching languages:', error);
    return languages; // Fallback to static data
  }
};